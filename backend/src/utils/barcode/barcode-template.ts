export function normalizeCode39Value(value: string) {
  return value
    .trim()
    .toUpperCase()
    .replace(/[^0-9A-Z .\\/$+%-]/g, "");
}

export function encodeCode39(value: string) {
  const normalizedValue = normalizeCode39Value(value);

  if (!normalizedValue) {
    throw new Error("Barcode value is empty.");
  }

  const encodedCharacters = `*${normalizedValue}*`.split("").map((character) => {
    const pattern = CODE39_PATTERNS[character];

    if (!pattern) {
      throw new Error(`Unsupported barcode character: ${character}`);
    }

    return pattern;
  });

  return {
    value: normalizedValue,
    patterns: encodedCharacters,
  };
}
